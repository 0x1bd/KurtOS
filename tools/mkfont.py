def main():
    order = [chr(c) for c in range(0x20, 0x7F)]
    missing = [c for c in order if c not in G]
    if missing:
        print("missing:", missing)
        return 1

    if "--preview" in sys.argv:
        for c in order:
            print("--- %r ---" % c)
            for r in G[c]:
                print("  " + r.replace('#', '█').replace('.', ' '))
        return 0

    lines = []
    lines.append("package kernel.console")
    lines.append("")
    lines.append("object Font {")
    lines.append("    const val WIDTH = 5")
    lines.append("    const val HEIGHT = 8")
    lines.append("    const val ADVANCE = 6")
    lines.append("")
    lines.append("    fun glyph(c: Char): ByteArray {")
    lines.append("        val index = c.code - 0x20")
    lines.append("        if (index < 0 || index >= GLYPHS.size) return GLYPHS[0]")
    lines.append("        return GLYPHS[index]")
    lines.append("    }")
    lines.append("")
    lines.append("    private fun g(vararg rows: Int): ByteArray {")
    lines.append("        val bytes = ByteArray(HEIGHT)")
    lines.append("        for (i in 0 until HEIGHT) bytes[i] = rows[i].toByte()")
    lines.append("        return bytes")
    lines.append("    }")
    lines.append("")
    lines.append("    private val GLYPHS: Array<ByteArray> = arrayOf(")

    for c in order:
        vals = []
        for r in G[c]:
            bits = int(r.replace('.', '0').replace('#', '1'), 2)
            vals.append("0x%02X" % (bits << 3))
        lines.append("        g(%s)," % ", ".join(vals))

    lines.append("    )")
    lines.append("}")

    with open(sys.argv[1], "w") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote %s (%d glyphs)" % (sys.argv[1], len(order)))
    return 0


if __name__ == "__main__":
    sys.exit(main())
